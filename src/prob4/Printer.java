package prob4;

public class Printer {
	public void println(int str) {
		System.out.println(str);
	}

	public void println(boolean str) {
		System.out.println(str);
	}

	public void println(double str) {
		System.out.println(str);
	}

	public void println(String str) {
		System.out.println(str);
	}
}
